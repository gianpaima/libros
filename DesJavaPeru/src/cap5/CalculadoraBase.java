package cap5;

public class CalculadoraBase {

    public int suma (int n1, int n2)
    {
        int rpta = n1 + n2;
        return rpta;
    }

    public double suma (double n1, double n2)
    {
        double rpta = n1 + n2;
        return rpta;
    }

    public int resta (int n1, int n2)
    {
        int rpta = n1 - n2;
        return rpta;
    }

    public double resta (double n1, double n2)
    {
        double rpta = n1 - n2;
        return rpta;
    }


}
